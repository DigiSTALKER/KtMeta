    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            String item = (String) this.jComboBox1.getSelectedItem();
            switch (item) {
                case "Arc":
                    System.out.println("Arc selected.");
                    try {
                        UIManager.setLookAndFeel("com.formdev.flatlaf.intellijthemes.FlatArcIJTheme");
                    } catch (Exception e) {
                        System.out.println(e);
                    }
                    break;
                case "Carbon":
                    System.out.println("Carbon selected.");
                    try {
                        UIManager.setLookAndFeel("com.formdev.flatlaf.intellijthemes.FlatCarbonIJTheme");
                    } catch (Exception e) {
                        System.out.println(e);
                    }
                    break;
                case "Cobalt 2":
                    System.out.println("Cobalt 2 selected.");
                    try {
                        UIManager.setLookAndFeel("com.formdev.flatlaf.intellijthemes.FlatCobalt2IJTheme");
                    } catch (Exception e) {
                        System.out.println(e);
                    }
                    break;
                default:
                    FlatSolarizedLightIJTheme.install();
                    break;
            }
            SwingUtilities.updateComponentTreeUI(this);
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

